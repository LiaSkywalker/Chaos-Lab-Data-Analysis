%% This code plotting the Voltage on the diode as a function of time
%put this script in the same direcory the data in.
%% AC discreate measurements!

%% import Measurements
files=dir('*.csv');
Vsource =[]; %שמות קבצים X
Vmeasure=[]; %פיקים Y

for k=1:length(files) %repeat for every file
    [t,V]=importfile(files(k).name);%import data to tuple vector
    currentVoltage =str2double(files(k).name(1:end-4));
    
    Y= V; %מדידה 
    x= t; % זמן
    Y=Y+abs(min(Y));
    Y=smooth(smooth(Y));
    localmax=findpeaks(Y,'MinPeakProminence',0.1);
    for i=1:length(localmax) %add file name to the Vsource vetor
        Vsource(end+1)=currentVoltage;%שם הקובץ שזה האיבר הנוסף לוקטור מקור
    end
    Vmeasure= [Vmeasure;localmax];

end

plot(Vsource,Vmeasure,'.')

